UPD_REGEX = [
    # ========== ПЕРВОЕ СЛОВО: УНИВЕРСАЛЬНЫЙ ==========
    [
        # Базовые варианты
        r'(?:^|[^\w-])[Уу]ниверс[а-яё]*',
        r'\b[Уу]ниверсальн[а-яёй]*\b',
        r'[Уу]ниверсальн[а-яё]{0,4}',
        r'универсальн\w*',
        r'[УUуu]ниверс[а-яa-zё]*',

        # С падежами
        r'\b[Уу]ниверсальн(?:ого|ому|ым|ом|ая|ой|ую|ою|ое|ые|ых|ыми)\b',

        # Для OCR ошибок
        r'\b[Уу][нnh][иiu][вbв][еe][рp][сc][аa][лль][ьb]?[нnh]?[ыiyаaоo]?[йi]?\b',
    ],

    # ========== ВТОРОЕ СЛОВО: ПЕРЕДАТОЧНЫЙ ==========
    [
        # Базовые варианты
        r'(?:^|[^\w-])[Пп]ередаточн[а-яё]*',
        r'(?:^|[^\w-])[Пп]ередачн[а-яё]*',  # без "о"
        r'[Пп]ередаточн[а-яё]{0,4}',
        r'передаточн\w*',
        r'[ПPпp]ереда[тtч]очн[а-яa-zё]*',

        # Сокращения и вариации
        r'\b[Пп]ереда?\.?\s*(?:[тч]оч?н[а-яё]*|[ао]ч?н[а-яё]*)?\b',

        # С падежами
        r'\b[Пп]ередаточн(?:ого|ому|ым|ом|ая|ой|ую|ою|ое|ые|ых|ыми)\b',

        # Для OCR ошибок
        r'\b[Пpn][еe][рp][еe][дd][аa][тt][оo0][чqч][нnh][ыiyаaоo]?[йi]?\b',
    ],

    # ========== ТРЕТЬЕ СЛОВО: ДОКУМЕНТ ==========
    [
        # Базовые варианты
        r'(?:^|[^\w-])[Дд]окумент[а-яё]*',
        r'\b[Дд]ок[у]?[у]?мент[а-яё]*',
        r'документ\w*',
        r'(?:[Дд]окумент[а-яё]*|[Аа]кт[а-яё]*|[Фф]орм[а-яё]*)',
        r'(?:[ДDдd]окумент[а-яa-zё]*|UPD|УПД)',

        # С падежами
        r'\b[Дд]окумент(?:а|у|ом|е|ы|ов|ам|ами|ах)?\b',

        # Для OCR ошибок
        r'\b[ДdD][оo0О][кkKк][уyYу][мmMм][еeEе][нnhH][тtT][аaоoыу]?\b',
    ]
]